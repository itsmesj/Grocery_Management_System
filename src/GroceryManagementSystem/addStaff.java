    public static boolean isValidMobileNo(String str)  {  
	Pattern ptrn = Pattern.compile("(0/91)?[6-9][0-9]{9}");  
	Matcher match = ptrn.matcher(str);  
	return (match.find() && match.group().equals(str));  
}  
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jComboBox1.getSelectedIndex()==0 || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() ) && (jButtonGroup1.isSelected() || jRadioButton2.isSelected())){
            
        DateTimeFormatter cur_dt = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();  
        String dt = String.valueOf(cur_dt.format(now));
        
        String st_name = jTextField1.getText();
        String ph = jTextField2.getText();
        String add = jTextField5.getText();
        String dpt_name = jComboBox1.getSelectedItem().toString();
        String sal = jTextField4.getText();
        String gender="";
        if(jButtonGroup1.isSelected()){
            gender = "Male";
        }  
        if(jRadioButton2.isSelected()){
            gender = "Female";
        }       
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/grocerydb", "root", "");
            String q1 = "insert into staff(staff_name, phone, address, dept_name, salary, joining_date, gender) values(?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement p = con.prepareStatement(q1);
            p.setString(1, st_name);
            p.setString(2, ph);
            p.setString(3, add);
            p.setString(4, dpt_name);
            p.setString(5, sal);
            p.setString(6, dt);
            p.setString(7, gender);
            if(jTextField1.getText().length() >= 4){
            if(jTextField5.getText().length() >= 6){
            if(isValidMobileNo(ph)){
                    if(Double.parseDouble(sal) > 5000){
                    int x = p.executeUpdate();
                    JOptionPane.showMessageDialog(this, x+" Staff Added!");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jTextField4.setText("");
                    jTextField5.setText("");
                    
                    
                    } else {
                        JOptionPane.showMessageDialog(this, "Minimum Salary can be atleast Rs.5000!");
                    }
                
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Phone number entered!");
            }
            } else {
                JOptionPane.showMessageDialog(this, "ADDRESS ERROR: An address field must contain atleast 6 characters!");
            }
            } else {
                JOptionPane.showMessageDialog(this, "NAME ERROR: A name field must contain atleast 4 characters");
            }
            con.close();
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        } else {
            JOptionPane.showMessageDialog(this, "All fields must be filled.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

